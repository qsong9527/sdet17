# 需求
#  实现输入两个数字，计算出这两个数字的和、差、积、商、余
# 例：
# > 请输入数字1：6
# > 请输入数字2：3
# > 6 + 3 = 9
# > 6 - 3 = 3
# > 6 * 3 = 18
# > 6 / 3 = 2
# > 6 % 3 = 0
a = int(input("请输入数字1："))
b = int(input("请输入数字2："))
print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")
print(f"{a} / {b} = {a / b}")
print(f"{a} % {b} = {a % b}")
